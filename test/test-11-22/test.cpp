//1. 构造函数：对象的 “初始化器”
//作用：
//创建对象时自动调用，初始化对象的成员变量（默认版本仅做 “浅初始化”，比如内置类型不初始化，自定义类型调用其默认构造）。
//核心特性：
//函数名与类名相同，无返回值（连 void 都没有）；
//可重载（支持多个构造函数，按参数匹配调用）；
//若用户未显式定义任何构造函数，编译器才生成「默认构造函数」（无参数）；
//隐含 this 指针，指向当前正在创建的对象。
//
